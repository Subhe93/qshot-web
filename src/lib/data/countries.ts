/**
 * Full country calling-code dataset for the phone-number picker, mirroring the
 * mobile `country_picker` package. Country display names are resolved at
 * runtime via `Intl.DisplayNames` so they follow the active locale.
 */
export interface Country {
  iso: string; // ISO 3166-1 alpha-2
  dial: string; // calling code without leading "+"
}

/** Flag emoji derived from the ISO country code (regional indicator letters). */
export function flagEmoji(iso: string): string {
  return iso
    .toUpperCase()
    .replace(/./g, (c) => String.fromCodePoint(127397 + c.charCodeAt(0)));
}

/** Localized country name (falls back to the ISO code). */
export function countryName(iso: string, locale = "en"): string {
  try {
    return new Intl.DisplayNames([locale], { type: "region" }).of(iso) ?? iso;
  } catch {
    return iso;
  }
}

